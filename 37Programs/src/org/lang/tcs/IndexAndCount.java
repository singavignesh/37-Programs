package org.lang.tcs;

public class IndexAndCount {
	public static void main(String[] args) {
		int a[] = {10,20,30,20,40,10,70,80};
		int count = 0;
		for (int i = 0; i < a.length; i++) {
			if(a[i]==20) {
				count = count+1;
					
					
			}
			
				
			}
		System.out.println(count);
		}
	}


